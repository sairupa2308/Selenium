package JavaBasics;

public class A {


  public A() {
	  System.out.println("parent class method");
  }
  public A(int i) {
	  System.out.println("parameter of i "+ i);
  }
  public A(int i,int j) {
	  System.out.println("parameter of i "+ i);
	  System.out.println("parameter of j "+ j);
		 
  }
}
